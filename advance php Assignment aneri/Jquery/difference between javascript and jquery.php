                <h1 align="center">Difference Between JavaScript And JQuery</h1>
                <tr>
                    <th><b>Points</b></th>
                    <th><b>JavaScript</b></th>
                    <th><b>JQuery</b></th>
                </tr>
    
                <tr>
                    <td>Existance</td>
                    <td>JavaScript is an independent language and can exist on its own.</td>
                    <td>jQuery is a JavaScript library. It would not have been invented had JavaScript was not there. jQuery is still dependent on JavaScript as it has to be converted to JavaScript for the browser in-built JavaScript engine to interpret and run it.</td>
                </tr>
                <tr>
                    <td>Language</td>
                    <td>It is a high-level interpreted client-side scripting language. This is a combination of ECMA script and DOM (Document Object Model).</td>
                    <td>It is a light-weight JavaScript library. It has only the DOM.</td>
                </tr>
                <tr>
                    <td>Coding</td>
                    <td>JavaScript uses more lines of code as we have to write our own code.</td>
                    <td>jQuery uses fewer lines of code than JavaScript for the same functionality as the code is already written in its library, we just have to import the library and use the relevant function/method of the library in our code.</td>
                </tr>
                <tr>
                    <td>Usage</td>
                    <td>JavaScript code is written inside the script tag in a HTML page.</td>
                    <td>We need to import the jQuery from CDN or from a location where the jQuery library is downloaded in order to use it. jQuery code is also written inside the script tag on the HTML page.</td>
                </tr>
                <tr>
                    <td>Animation</td>
                    <td>We can make animations in JavaScript with many lines of code. Animations are mainly done by manipulating the style of an Html page.</td>
                    <td>In jQuery, we can add animation effects easily with fewer lines of code.</td>
                </tr>
                <tr>
                    <td>User-friendliness</td>
                    <td>JavaScript can be cumbersome for the developer as it can take a number of lines of code to attain a functionality.</td>
                    <td>jQuery is more user-friendly than JavaScript with few lines of code.</td>
                </tr>
                <tr>
                    <td>Performance</td>
                    <td>Pure JavaScript can be faster for DOM selection/manipulation than jQuery as JavaScript is directly processed by the browser.</td>
                    <td>jQuery has to be converted into JavaScript to make it run in a browser.</td>
                </tr>
                <tr>
                    <td>Verbosity</td>
                    <td>JavaScript is verbose as one has to write many lines of code for a functionality.</td>
                    <td>jQuery is concise and uses fewer lines of code, sometimes only one line of code.</td>
                </tr>
                <tr>
                    <td>Size and Weight</td>
                    <td>Being a language, it is heavier than jQuery.</td>
                    <td>Being a library, it is lightweight. It has a minified version of its code which makes it light-weight.</td>
                </tr>
                <tr>
                    <td>Reusability and Maintainability</td>
                    <td>JavaScript code can be verbose and therefore can be difficult to maintain and reuse.</td>
                    <td>With fewer lines of code, jQuery is more maintainable as we just have to call the predefined functions in the jQuery library in our code. This also makes us to easily reuse jQuery functions at different places in the code.</td>
                </tr>
